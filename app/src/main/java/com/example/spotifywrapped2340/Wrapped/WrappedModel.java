package com.example.spotifywrapped2340.Wrapped;

public class WrappedModel {

    String title;
    String firstElement;
    String secondElement;
    String thirdElement;
    String fourthElement;
    String fifthElement;

    public WrappedModel(String title, String firstElement, String secondElement, String thirdElement, String fourthElement, String fifthElement) {
        this.title = title;
        this.firstElement = firstElement;
        this.secondElement = secondElement;
        this.thirdElement = thirdElement;
        this.fourthElement = fourthElement;
        this.fifthElement = fifthElement;
    }


    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getFirstElement() {
        return firstElement;
    }

    public void setFirstElement(String firstElement) {
        this.firstElement = firstElement;
    }

    public String getSecondElement() {
        return secondElement;
    }

    public void setSecondElement(String secondElement) {
        this.secondElement = secondElement;
    }

    public String getThirdElement() {
        return thirdElement;
    }

    public void setThirdElement(String thirdElement) {
        this.thirdElement = thirdElement;
    }

    public String getFourthElement() {
        return fourthElement;
    }

    public void setFourthElement(String fourthElement) {
        this.fourthElement = fourthElement;
    }

    public String getFifthElement() {
        return fifthElement;
    }

    public void setFifthElement(String fifthElement) {
        this.fifthElement = fifthElement;
    }


}
